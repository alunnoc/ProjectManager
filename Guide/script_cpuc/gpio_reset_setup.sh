#!/bin/bash

#set GPIO as output
echo "Set GPIO as output"
i2cset -y 0 0x20 0x6 0x78
i2cset -y 0 0x20 0x7 0xff

#set GPIO value to 1
echo "Set GPIO value to 1"
i2cset -y 0 0x20 0x2 0xff
i2cset -y 0 0x20 0x3 0xff

#set GPIO value to 0
echo "Set GPIO value to 0"
i2cset -y 0 0x20 0x2 0x00
i2cset -y 0 0x20 0x3 0x00

sleep 1

#set GPIO value to 1
echo "Set GPIO value to 1"
i2cset -y 0 0x20 0x2 0xff
i2cset -y 0 0x20 0x3 0xff

exit 0
